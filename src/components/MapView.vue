<template>
  <div ref="map" class="map">
  </div>
</template>

<script>
import mapboxgl from 'mapbox-gl';
import constants from '../util/constants';

mapboxgl.accessToken = process.env.VUE_APP_MAPBOX_TOKEN;

export default {
  name: 'map-view',
  data: function() {
    return {
      lng: 5,
      lat: 34,
      zoom: 1.5
    };
  },
  mounted: function() {
    const vm = this;
    const map = new mapboxgl.Map({
      container: vm.$refs.map,
      style: 'mapbox://styles/mapbox/streets-v9',
      center: [vm.lng, vm.lat],
      zoom: this.zoom
    });
    map.on('load', () => {
      map.addLayer({
        id: 'education',
        type: 'circle',
        source: {
          type: 'geojson',
          data: constants.URL_EDUCATION
        },
        paint: {
          'circle-radius': {
            'base': 1.75,
            'stops': [[12, 2], [22, 180]]
          },
          'circle-color': '#fa824c'
        }
      });
      map.addLayer({
        id: 'places',
        type: 'circle',
        source: {
          type: 'geojson',
          data: constants.URL_POPULATED_PLACES
        },
        paint: {
          'circle-radius': {
            'base': 1.75,
            'stops': [[12, 4], [22, 360]]
          },
          'circle-color': '#3c91e6',
          'circle-opacity': 0.6
        }
      });
    });    
  }
};
</script>

